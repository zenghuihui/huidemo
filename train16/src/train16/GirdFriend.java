package train16;

public class GirdFriend {
	private int face;
	private int weight;
	private int tall;
	private int shengqi;
	private int love;
	public int getFace() {
		return face;
	}
	public void setFace(int face) {
		this.face = face;
	}
	public int getWeight() {
		return weight;
	}
	public void setWeight(int weight) {
		this.weight = weight;
	}
	public int getTall() {
		return tall;
	}
	public void setTall(int tall) {
		this.tall = tall;
	}
	public int getShengqi() {
		return shengqi;
	}
	public void setShengqi(int shengqi) {
		this.shengqi = shengqi;
	}
	public int getLove() {
		return love;
	}
	public void setLove(int love) {
		this.love = love;
	}
}
